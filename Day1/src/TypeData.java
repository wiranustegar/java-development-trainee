public class TypeData {
    public static void main(String[] args) {

        byte contohByte = 123;
        byte contohByteMinus = -121;

        System.out.println("Contoh byte terbesar : " + contohByte);
        System.out.println("Contoh byte terkecil : " + contohByteMinus);

        short contohShort = 1000;
        int contohInt = 10000;
        long contohLong = 1000000;

        System.out.println("Contoh short : " + contohShort);
        System.out.println("Contoh int : " + contohInt);
        System.out.println("Contoh long : " + contohLong);

        float contohPi = 3.14f;
        double contohDouble = 12.5421;
        double pi = Math.PI;

        System.out.println("Contoh Float : " + contohPi);
        System.out.println("Contoh Double" + contohDouble);
        System.out.println("Contoh math.pi" + pi);

        boolean contohBenar = true;
        boolean contohSalah = false;

        System.out.println("Contoh Benar : " + contohBenar);
        System.out.println("Contoh Salah : " + contohSalah);

        char contohHuruf = 'c';
        char contohAscii = 120;

        System.out.println("Contoh Char huruf : " + contohHuruf);
        System.out.println("Contoh Char ascii : " + contohAscii);

        int huruf = 'x';
        System.out.println(huruf);

    }
}
