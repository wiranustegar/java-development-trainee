public class IncrementDecrement {
    public static void main(String[] args) {

        int b = 20;;
        b++;
        ++b;
        b--;
        --b;

        System.out.println("b: " + b);

        int c = 100;
        int cplus = c++;
        System.out.println(cplus);

        int d = 90;
        int dplus = ++d;
        System.out.println(dplus);

    }
}
