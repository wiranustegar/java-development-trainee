public class DemoArray2 {
    public static void main(String[] args) {

        String daftarNama [];
        daftarNama = new String[3];
        daftarNama[0] = "Amir";
        daftarNama[1] = "Budi";
        daftarNama[2] = "Charlie";

        System.out.println("Jumlah ELement = " + daftarNama.length);
        System.out.println("daftarNama[0] = " + daftarNama[0]);
        System.out.println("daftarNama[1] = " + daftarNama[1]);
        System.out.println("daftarNama[2] = " + daftarNama[2]);

        String daftarKota [];
        daftarKota = new String[] {"jakarta", "bandung", "semarang", "jogja"};

        System.out.println("Jumlah Element = " + daftarKota.length);
        System.out.println("daftarKota[0] = " + daftarKota[0]);
        System.out.println("daftarKota[1] = " + daftarKota[1]);
        System.out.println("daftarKota[2] = " + daftarKota[2]);
        System.out.println("daftarKota[3] = " + daftarKota[3]);

    }
}
